$(document).ready(function() {
    $("#block1").click(function() {
        $(this).toggleClass("active");

        if ($(this).hasClass("active")) {
            $(this).css("opacity", "1");
        } else {
            $(this).css("opacity", "0.5");
        }
    });
});

